package com.example.stu.sqlitedemo172065ljb;

public class Infor {
    private int id;
    private String name;
    private Long phone;
    public Infor() {   }
    public int get_id() {
        return id;
    }
    public void setId(int id) {

        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPhone() {
        return phone;
    }

    public void setPhone(Long phone) {
        this.phone = phone;
    }
}
